package edu.quipu.rrhh.models;

public class Rechazados {
    String dni;
    Integer ud;
    Integer modcod;
    String obs;

    public String getDni() {
        return dni;
    }

    public void setDni(String dni) {
        this.dni = dni;
    }

    public Integer getUd() {
        return ud;
    }

    public void setUd(Integer ud) {
        this.ud = ud;
    }

    public Integer getModcod() {
        return modcod;
    }

    public void setModcod(Integer modcod) {
        this.modcod = modcod;
    }

    public String getObs() {
        return obs;
    }

    public void setObs(String obs) {
        this.obs = obs;
    }



}

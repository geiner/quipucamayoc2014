package edu.quipu.rrhh.models;

public class tipoResolucion {
    private String tipResu;
    private String descriResu;
    private int tipoRe;

    public String getTipResu() {
        return tipResu;
    }

    public void setTipResu(String tipResu) {
        this.tipResu = tipResu;
    }

    public String getDescriResu() {
        return descriResu;
    }

    public void setDescriResu(String descriResu) {
        this.descriResu = descriResu;
    }

    public int getTipoRe() {
        return tipoRe;
    }

    public void setTipoRe(int tipoRe) {
        this.tipoRe = tipoRe;
    }
}
